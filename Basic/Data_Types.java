package Basic;

public class Data_Types {
	public static void main(String[] args) {
		// Data Types
		int i = 10;
		float f = 3.14f;
		double d = 234535.45;
		long l = 343325435453436L;
		char c = 'H';
		String s = "Het Tamboli";
		
		System.out.println(
				"Integer value is: " + i 
				+ "\n\nFloat value is: " + f
				+ "\n\nDouble value is: " + d
				+ "\n\nLond value is: " + l
				+ "\n\nChar value is: " + c
				+ "\n\nString value is: " + s
				);
	}
}